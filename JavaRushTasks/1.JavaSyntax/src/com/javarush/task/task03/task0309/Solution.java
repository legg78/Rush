/* 
Сумма 5 чисел
*/

public class Solution {
    public static void main(String[] args) {
        //напишите тут ваш код
        int res = 0;
        for (int ii = 1; ii <= 5; ii++) {
            res += ii;
            System.out.println(res);
        }

    }
}
